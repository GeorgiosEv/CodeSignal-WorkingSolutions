""" Write a function that returns the sum of two numbers.

Example:
For param1 = 1 and param2 = 2, the output should be add(param1, param2) = 3. """
def add(param1, param2):
    # A pretty straightforward exercise. A function including 2 parameters, which are summed together, returning the sum as the result.
    sum = param1 + param2
    return sum
